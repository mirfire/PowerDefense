package entities;

public class Resistances {
	private int resistNormal;
	private int resistNetwork;
	private int resistFile;
	private int resistWeb;
	private int resistCode;
	
	public int getResistNormal() {
		return resistNormal;
	}
	public void setResistNormal(int resistNormal) {
		this.resistNormal = resistNormal;
	}
	public int getResistNetwork() {
		return resistNetwork;
	}
	public void setResistNetwork(int resistNetwork) {
		this.resistNetwork = resistNetwork;
	}
	public int getResistFile() {
		return resistFile;
	}
	public void setResistFile(int resistFile) {
		this.resistFile = resistFile;
	}
	public int getResistWeb() {
		return resistWeb;
	}
	public void setResistWeb(int resistWeb) {
		this.resistWeb = resistWeb;
	}
	public int getResistCode() {
		return resistCode;
	}
	public void setResistCode(int resistCode) {
		this.resistCode = resistCode;
	}
	
}
